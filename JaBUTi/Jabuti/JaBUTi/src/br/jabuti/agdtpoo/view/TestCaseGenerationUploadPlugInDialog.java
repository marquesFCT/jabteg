    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
       
        JFileChooser fc = new JFileChooser(".");
        FileFilter filter = new ExtensionFileFilter("JAR", new String[] {"JAR"});
        
        fc.setFileFilter(filter);
        
        int returnVal = fc.showOpenDialog(this.txtPlugInPath);
        
        if (returnVal == JFileChooser.APPROVE_OPTION)
        {
            File file = fc.getSelectedFile();
            
            this.txtPlugInPath.setText(file.getPath());
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void btnInstallActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnInstallActionPerformed
        
        if (this.txtPlugInName.getText().equals(""))
        {
            showValidationErros("Define the name of the new test data generation plug-in.");
            return;
        }
        
        if (this.txtPlugInPath.getText().equals(""))
        {
            showValidationErros("Define the path of JAR file used to install the new test data generation plug-in.");
            return;
        }
        
        // Cria nova inst√¢ncia do plug-in a ser persistido
        TestDataGenerationPlugIn newPlugIn = new TestDataGenerationPlugIn();
        newPlugIn.JarPath = this.txtPlugInPath.getText();
        newPlugIn.Name = this.txtPlugInName.getText();
        
        if (TestCaseGenerationUploadPlugInController.verifyValidPlugIn(newPlugIn))
        {
            TestCaseGenerationUploadPlugInController.savePlugIn(newPlugIn);
            
            if (this._menuBuilder != null)
            {
                // Adicionar o novo icone ao menu de plug-ins
                this._menuBuilder.addNewPlugInMenuItem(newPlugIn);
            }
            
            showInformation("Plug-in installed.");
            
            this.dispose();
        }
        else
        {
            showValidationErros("Invalid plug-in name, path or format. Try again.");
            return; 
        }
    }//GEN-LAST:event_btnInstallActionPerformed

    private void showValidationErros(String message)
    {
        JOptionPane.showMessageDialog(rootPane, message, FORMLABEL, JOptionPane.WARNING_MESSAGE);
    }
    
    private void showInformation(String message)
    {
        JOptionPane.showMessageDialog(rootPane, message, FORMLABEL, JOptionPane.INFORMATION_MESSAGE);
    }
    
