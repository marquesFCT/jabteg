
package br.usp.each.saeg.agdtpoo.entity;

public class BytecodeType {
    private String _typeName;
    private ReflectionClass _currentClass;
    
    public void setCurrentClass(ReflectionClass currentClass)
    {
        this._currentClass = currentClass;
    }
    
    public ReflectionClass getCurrentClass()
    {
        return this._currentClass.clone();
    }
    
    public void setTypeName(String typeName)
    {
        this._typeName = typeName;
    }
    
    public String getTypeName()
    {
        return this._typeName;
    }
}
