
package br.usp.each.saeg.agdtpoo.entity;

import java.lang.reflect.Constructor;
import java.lang.reflect.Method;

public class JavaClass {
    
    private String _className;
    private Constructor[] _constructors;
    private Method[] _methods;
    private Class _classUnderTest;
    
    public Class getClassUnderTest()
    {
        return this._classUnderTest;
    }
    
    public Method[] getMethods()
    {
        return this._methods;
    }
    
    public Constructor[] getConstructors()
    {
        return this._constructors;
    }
    
    public String getClassName()
    {
        return this._className;
    }
    
    public void setConstructors(Constructor[] constructors)
    {
        this._constructors = constructors;
    }
    
    public void setMethods(Method[] methods)
    {
        this._methods = methods;
    }
    
    public void setClassUnderTest(Class classUnderTest)
    {
        this._classUnderTest = classUnderTest;
    }
    
    public void setClassName(String name)
    {
        this._className = name;
    }
}
